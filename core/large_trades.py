"""Лента крупных сделок: порог по скользящему процентилю.

Задача — показывать не «все сделки подряд» (по BTC их 470 000 в сутки,
читать это невозможно), а только те, что реально двигают цену.

ПОЧЕМУ ПОРОГ ОТНОСИТЕЛЬНЫЙ, А НЕ В ДОЛЛАРАХ. Абсолютный порог пришлось бы
задавать отдельно каждому из 12 инструментов и подкручивать при смене
режима рынка. Распределение размеров с тяжёлым хвостом: по BTC медиана
0.00117, p95 = 0.03 (в 25 раз больше), максимум 37.99. Порог «выше 95%
остальных за час» подстраивается сам — и под инструмент, и под текущую
волатильность.

КАК СЧИТАЕТСЯ ПРОЦЕНТИЛЬ. Точная сортировка окна отпадает: в часовом окне
по BTC ~32 000 сделок, а приходят они до 9 раз в секунду — сортировать на
каждой значило бы отдавать этому миллисекунды в потоке реального времени.
Кеш с периодическим пересчётом тоже плох, но по другой причине: он отстаёт
ровно в тот момент, когда начинается всплеск крупных сделок, то есть когда
лента нужнее всего.

Поэтому — гистограмма по логарифмической сетке размеров. Добавление и
удаление за O(1), процентиль берётся накопительной суммой по корзинам и
всегда актуален. Точность ограничена шириной корзины (~1%), систематического
смещения нет.

Окно хранит ИНДЕКСЫ корзин, а не размеры: при вытеснении старой сделки надо
знать лишь, какой счётчик уменьшить. Целое вместо float — вчетверо меньше
памяти на те же 12 × 32 000 записей.
"""

import collections
import math

# Шаг сетки размеров: каждая следующая корзина на 1% больше предыдущей.
# ~1% — предел точности процентиля, и этого хватает: порог всё равно
# ориентир, а не юридический факт.
SIZE_STEP = 0.01

# Опорный размер сетки. Как и в профиле объёма, значение важно лишь своей
# фиксированностью.
REFERENCE_SIZE = 1e-8

# Длина скользящего окна по умолчанию.
WINDOW_SEC = 3600

# Доля сделок, которые считаются НЕ крупными. 0.95 = «крупнее 95% остальных».
DEFAULT_PERCENTILE = 0.95

# Пока в окне меньше этого числа сделок, лента молчит. Процентиль по трём
# точкам — не статистика: в выходные по XAG (19% активных минуток против
# 76% в будни) он объявил бы «крупной» случайную мелочь.
MIN_SAMPLES = 200

# Через сколько изменений окна пересчитывать порог. При 25 000 сделок в окне
# одна сделка двигает p95 на доли процента — считать его каждый раз незачем,
# а стоит это 40% времени обработки (замерено профилировщиком).
THRESHOLD_REFRESH = 64


def size_bucket(size):
    """Номер корзины гистограммы для размера сделки.

    Args:
        size: Размер сделки в базовых единицах (> 0).

    Returns:
        Целочисленный индекс корзины. Размеры <= 0 попадают в корзину 0:
        уронить ленту из-за странной сделки хуже, чем неточно её учесть.
    """
    if size <= 0:
        return 0
    return int(math.floor(math.log(size / REFERENCE_SIZE) / math.log(1.0 + SIZE_STEP)))


def bucket_floor(index):
    """Нижняя граница корзины.

    Args:
        index: Индекс корзины.

    Returns:
        Float: минимальный размер, попадающий в эту корзину.
    """
    return REFERENCE_SIZE * ((1.0 + SIZE_STEP) ** index)


def bucket_ceiling(index):
    """Верхняя граница корзины — она же порог «крупной» сделки.

    Порогом берётся именно верх, а не низ. Причина — вырожденное окно:
    размеры сделок дискретны (по BTC 23.7% всех сделок имеют размер ровно
    0.0002 — боты с фиксированным лотом), и корзина на пороге бывает
    забита одинаковыми сделками. Взяв нижнюю границу, мы объявили бы
    крупной КАЖДУЮ из них — лента залилась бы целиком.

    Args:
        index: Индекс корзины.

    Returns:
        Float: размер, который надо превысить, чтобы считаться крупным.
    """
    return REFERENCE_SIZE * ((1.0 + SIZE_STEP) ** (index + 1))


class LargeTradeFilter:
    """Скользящий процентиль размера сделок по одному инструменту.

    Использование::

        flt = LargeTradeFilter()
        if flt.add(size, ts):      # add возвращает True для крупной сделки
            show_in_feed(...)
    """

    def __init__(self, window_sec=WINDOW_SEC, percentile=DEFAULT_PERCENTILE,
                 min_samples=MIN_SAMPLES, min_size=0.0):
        """Создать фильтр.

        Args:
            window_sec:  Длина скользящего окна в секундах.
            percentile:  Доля сделок, считающихся не крупными (0..1).
            min_samples: Сколько сделок должно накопиться, прежде чем фильтр
                         начнёт что-либо пропускать.
            min_size:    Абсолютный минимум размера. Страховка от вырожденного
                         случая на мёртвом инструменте, где «крупной» стала бы
                         сделка на два доллара. 0 = не применять.

        Returns:
            None.
        """
        self.window_sec = window_sec
        self.percentile = percentile
        self.min_samples = min_samples
        self.min_size = min_size

        self._window = collections.deque()   # (ts, bucket_index)
        self._counts = {}                    # bucket_index -> сколько в окне
        self._total = 0

        # Кеш порога. Профилирование на реальных сутках BTC показало, что
        # 94% времени уходило в threshold(), а внутри него 40% — в сортировку
        # ключей словаря на КАЖДОЙ сделке. При 25 000 сделок в окне одна
        # сделка сдвигает p95 на доли процента, поэтому порог пересчитывается
        # раз в THRESHOLD_REFRESH изменений окна.
        self._cached_threshold = None
        self._dirty = THRESHOLD_REFRESH   # первый вызов считает честно

    def _evict(self, now):
        """Выбросить из окна всё, что старше window_sec.

        Args:
            now: Текущее время, unix-секунды.

        Returns:
            None.
        """
        cutoff = now - self.window_sec
        window = self._window
        counts = self._counts
        while window and window[0][0] < cutoff:
            _, idx = window.popleft()
            remaining = counts.get(idx, 0) - 1
            if remaining > 0:
                counts[idx] = remaining
            else:
                # Пустые корзины удаляем: иначе словарь копил бы мусор за
                # всё время работы процесса (месяцы).
                counts.pop(idx, None)
            self._total -= 1
            self._dirty += 1

    def threshold(self):
        """Текущий порог «крупной» сделки.

        Пересчитывается не чаще, чем раз в THRESHOLD_REFRESH изменений окна;
        между пересчётами отдаётся кеш. Это не тот «устаревший кеш», что
        отстаёт на всплеске: обновление привязано к числу сделок, а не к
        времени, поэтому на всплеске порог пересчитывается ЧАЩЕ, а не реже.

        Returns:
            Float: размер, выше которого сделка считается крупной, либо None,
            если данных в окне ещё недостаточно.
        """
        if self._total < self.min_samples:
            return None

        if self._dirty < THRESHOLD_REFRESH and self._cached_threshold is not None:
            return self._cached_threshold

        target = self._total * self.percentile
        seen = 0
        level = None
        for idx in sorted(self._counts):
            seen += self._counts[idx]
            if seen >= target:
                # Верхняя граница корзины: крупной считается сделка строго
                # больше всего, что в этой корзине лежит. Лучше пропустить
                # пограничную сделку, чем залить ленту одинаковыми.
                level = max(bucket_ceiling(idx), self.min_size)
                break

        self._cached_threshold = level
        self._dirty = 0
        return level

    def add(self, size, ts):
        """Учесть сделку и сказать, крупная ли она.

        Сделка сравнивается с порогом, посчитанным по окну ДО неё: иначе
        каждая новая сделка сдвигала бы планку под себя.

        Args:
            size: Размер сделки в базовых единицах.
            ts:   Время сделки, unix-секунды.

        Returns:
            Кортеж (is_large, level): признак крупной сделки и ПОРОГ, ПО
            КОТОРОМУ ПРИНЯТО РЕШЕНИЕ. Порог возвращается здесь, а не берётся
            потом через threshold(): между решением и отчётом кеш успевает
            обновиться, и в событии оказывалась пара size <= threshold —
            выглядит как ошибка, хотя фильтр отработал верно.
        """
        self._evict(ts)
        level = self.threshold()

        idx = size_bucket(size)
        self._window.append((ts, idx))
        self._counts[idx] = self._counts.get(idx, 0) + 1
        self._total += 1
        self._dirty += 1

        if level is None:
            return False, None
        return size > level, level

    @property
    def samples(self):
        """Сколько сделок сейчас в окне.

        Returns:
            Int.
        """
        return self._total
