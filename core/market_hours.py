"""
Часы работы рынков — общий хелпер для фида и хаба.

Вынесен в core/, потому что нужен обоим процессам: фиду (watchdog не должен
реконнектить в закрытый рынок) и хабу (health/heartbeat не считают тишину
выходных деградацией). Импортировать из feeds/ в хаб нельзя — фид тянет
forexconnect (только py3.7); этот модуль чистый и работает на обоих питонах.

Синтаксис ограничен Python 3.7.
"""

from datetime import datetime


def forex_open(ts=None):
    """Открыт ли форекс-рынок в момент ts (UTC).

    Форекс торгуется с воскресенья ~22:00 UTC до пятницы ~22:00 UTC (закрытие
    в Нью-Йорке). Точные минуты у брокеров плавают; берём консервативно —
    задача не «угадать спред открытия», а не считать честно закрытый рынок
    (где тиков нет по определению) зависанием.

    Args:
        ts: Unix-время в секундах (None = сейчас).

    Returns:
        bool — рынок предположительно открыт.
    """
    import time
    dt = datetime.utcfromtimestamp(time.time() if ts is None else ts)
    wd = dt.weekday()           # 0=Пн … 6=Вс
    hr = dt.hour
    if wd == 5:                 # суббота — закрыт весь день
        return False
    if wd == 4 and hr >= 22:    # пятница после 22:00 UTC — закрыт
        return False
    if wd == 6 and hr < 22:     # воскресенье до 22:00 UTC — закрыт
        return False
    return True
